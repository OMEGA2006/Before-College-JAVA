public class Variables {
    public static void main(String[] args) {
        //variables
        int a = 10;
        int b = 5;


        int sum = a + b;
        System.out.println(sum);


        int diff = b - a;
        System.out.println(diff);


        int mul = a * b;
        System.out.println(mul);


        int calc = (a * b) / (a - b);
        System.out.println(calc);
    }
}
