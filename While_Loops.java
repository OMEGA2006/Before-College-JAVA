public class While_Loops {
    public static void main(String[] args) {

        int i = 0;
        while(i < 11){
        System.out.println(i);
        i++; //i = i + 1
        }

        int j = 0;
        while(j < 11){
        System.out.print(j + " ");
        j++; //j = j + 1
        }
    }
}